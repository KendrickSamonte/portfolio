
document.addEventListener("DOMContentLoaded", function () {
    const thrill = document.querySelector(".thrill");
    const video = thrill.querySelector(".thrillVid");

    thrill.addEventListener("mouseover", function () {
      video.play();
    });

    thrill.addEventListener("mouseout", function () {
      video.pause();
      video.currentTime = 0; 
    });
  });

